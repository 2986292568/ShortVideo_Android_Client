package com.wanyi.uiframe.installed.query;

public class InstalledQuery {
    //apk uuid
    String uuid;
    //device info
    String device;
    //android os
    String osver;
    //apk version
    String apkver;

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }

    public String getDevice() {
        return device;
    }

    public void setDevice(String device) {
        this.device = device;
    }

    public String getOsver() {
        return osver;
    }

    public void setOsver(String osver) {
        this.osver = osver;
    }

    public String getApkver() {
        return apkver;
    }

    public void setApkver(String apkver) {
        this.apkver = apkver;
    }




}
